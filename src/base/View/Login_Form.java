    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        //Button login user
        jLblLoginAttemptsLeft.setText("");//Login attempts lable text on initialization when they are 10

        if (logInAttempts >= 1) {//If no login attempts left exit
            PreparedStatement st = null;
            ResultSet rs = null;
            // get the username & password
            String username = jTextField_Username.getText();
            String password = String.valueOf(jPasswordField.getPassword());
            //SQL Query for username 
            String query = "SELECT * FROM `users` WHERE `username` = ? ";

            if (username.trim().equals("username")) {

                JOptionPane.showMessageDialog(null, "Enter Your Username", "Empty Username", 2);

            } else if (password.trim().equals("password")) {

                JOptionPane.showMessageDialog(null, "Enter Your Password", "Empty Password", 2);

            } else {

                try {
                    st = connct.prepareStatement(query);
                    st.setString(1, username);
                    rs = st.executeQuery();
                    if (rs.next()) {
                        String usr = rs.getString("username");
                        String key = rs.getString("password");
                        String salt = rs.getString("salt");
                        String name = rs.getString("full_name");
                        String id = rs.getString("id");

                        if (PasswordUtils.verifyPassword(password, key, salt)) {//If Login information is correct
                            //Log in the user if credentials are correct

                            //Gets logged in user's info for later use
                            LogInAs.empIdusr = usr;//Gets logged in user's username from the SQL DB table for later use
                            LogInAs.empName = name;//Gets logged in user's real name from the SQL DB table for later use
                            LogInAs.empId = id;//Gets logged in user's id number from the SQL DB table for later use

                            //Show a new form for menu
                            Menu_Form form = new Menu_Form();//New menu form
                            form.setVisible(true);//Make it visible
                            form.pack();
                            form.setLocationRelativeTo(null);//Centers the new menu form
                            logInAttempts = 10;//Sets login attempts back to defaut number
                            this.dispose(); // close the current form(login form)

                        } else {//If Login information is not correct
                            
                            //Error message
                            JOptionPane.showMessageDialog(null, "Invalid Username / Password", "Login Error", 2);
                            logInAttempts--;//Decremetns login attempts
                            jLblLoginAttemptsLeft.setText("");//Clears loggin attempts label
                            jLblLoginAttemptsLeft.setText(" Login attempts left : " + logInAttempts);//Dispalys loggin attempts left
                            //Reset to defaut login textfield and password field
                            jTextField_Username.setText("username");//Resets username defaut text
                            jPasswordField.setText("password");//Resets password defaut text
                        }
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                }

            }
        } else if (logInAttempts == 0) {//If no login attempts left exit
            //Message on 0 login attempts
            JOptionPane.showMessageDialog(null, "Contact your system administrator for moore information", "Login failed", 2);
            System.exit(0);//Stops the program
        }
    }//GEN-LAST:event_jButton_LoginActionPerformed

