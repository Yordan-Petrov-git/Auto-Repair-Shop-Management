    public boolean checkUsrEmail(String username, String email, String passphrase) {
//Checks if the user email alredy exists in the SQL DB
        PreparedStatement st = null;
        ResultSet rs = null;
        boolean username_exist = false;
//SQL Query
        String query = "SELECT * FROM `users` WHERE `users`.`username` =?  AND `users`.`email`=? AND `users`.`secretKey`=? ";

        try {

            st = connct.prepareStatement(query);
            st.setString(1, username);
            st.setString(2, email);
            st.setString(3, passphrase);
            rs = st.executeQuery();

            if (rs.next()) {
                username_exist = true;
            }

        } catch (SQLException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
        }
        return username_exist;
    }

    private static final String ALPHA_NUMERIC_STRING = "AaBbCcDdEeFfGgHhIiJjKkLlMmNnOoPpQqReSsTtUuVvWwXxYyZz0123456789";
    //Redeem  code generator generates Alpha Numeric String with selected lenght used for password recovery for user accounts

    private static String randomAlphaNumeric(int count) {
        //Redeem code generation with chosen length   
        StringBuilder builder = new StringBuilder();
        while (count-- != 0) {
            int character = (int) (Math.random() * ALPHA_NUMERIC_STRING.length());
            builder.append(ALPHA_NUMERIC_STRING.charAt(character));
        }
        return builder.toString();
    }

