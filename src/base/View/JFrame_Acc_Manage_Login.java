    private void jBtnUpdateUsrInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateUsrInfoActionPerformed
//Updte button that opens update user information form    
        PreparedStatement st = null;
        ResultSet rs = null;

        String username = LogInAs.empIdusr;
        String password = String.valueOf(jPasswordField_1.getPassword());

        String query = "SELECT * FROM `users` WHERE `username` = ? ";
        try {
            st = connct.prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            if (rs.next()) {
                String key = rs.getString("password");//hashed password
                String salt = rs.getString("salt");//password salt
                if (PasswordUtils.verifyPassword(password, key, salt)) {
                    // show a new form for ac management
                    JFrame_Acc_Manage logAcc = new JFrame_Acc_Manage();
                    logAcc.setVisible(true);//makes acc maange form visible
                    logAcc.pack();
                    logAcc.setLocationRelativeTo(null);//centers the new form
                    this.dispose();//Disposes of the form for password validation
                } else {
                    // error message on wrong password
                    JOptionPane.showMessageDialog(null, "Invalid Username / Password", "Login Error", 2);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
        }
    }//GEN-LAST:event_jBtnUpdateUsrInfoActionPerformed

